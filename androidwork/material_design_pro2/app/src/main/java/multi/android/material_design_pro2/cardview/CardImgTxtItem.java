package multi.android.material_design_pro2.cardview;

public class CardImgTxtItem {
    int img;
    String txt;

    public CardImgTxtItem(int img, String txt) {
        this.img = img;
        this.txt = txt;
    }

    public int getImg() {
        return img;
    }

    public String getTxt() {
        return txt;
    }
}
